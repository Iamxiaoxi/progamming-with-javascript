//a short for loop to count the number from one to five.
for (i = 1; i < 6; i++) {
    console.log(i)
}
console.log('Counting completed!');



//a short for loop to count down the number from five to one.
for (i = 5; i > 0; i--) {
    console.log(i)
}
console.log('Countdown finished!');



//a short while loop to count the number from one to five.
var i = 1;
while (i < 6) {
    console.log(i);
    i++;
}
console.log('Counting completed!');



//a short while loop to count down the number from five to one.
var i = 5;
while (i > 0) {
    console.log(i);
    i--;
}
console.log('Countdown finished!');